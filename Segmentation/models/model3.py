    backbone = resnet50(
        pretrained=True,
        replace_stride_with_dilation=[False, True, True], qf=qp, quantization=True, out_channel = out_channel, stride_val = stride_val )
    return_layers = {'layer4': 'out'}
    return_layers['layer3'] = 'aux'
    backbone = IntermediateLayerGetter(backbone, return_layers=return_layers)
    aux_classifier = FCNHead(1024, 21)
    classifier = DeepLabHead(2048, 21)
        
    model = DeepLabV3(backbone, classifier, aux_classifier)
    arch = 'deeplabv3_resnet50_coco'
